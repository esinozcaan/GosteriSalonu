public class GosteriSalonu {

    private String SalonAdi;
    private int SalonKapasitesi;
    private String SalonMevkisi;


    public void setSalonAdi(String salonAdi) {
        SalonAdi = salonAdi;
    }

    public void setSalonKapasitesi(int salonKapasitesi) {
        SalonKapasitesi = salonKapasitesi;
    }

    public void setSalonMevkisi(String salonMevkisi) {
        SalonMevkisi = salonMevkisi;
    }

    public String getSalonMevkisi() {
        return SalonMevkisi;
    }

    public String getSalonAdi() {
        return SalonAdi;
    }

    public int getSalonKapasitesi() {
        return SalonKapasitesi;
    }

}
